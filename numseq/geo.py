
def square(n):
    """Returns the nth of the numbers
    that can be arranged into square geometric
    shape"""
    return n ** 2


def triangle(n):
    """Returns the nth term of the numbers that
    can be arranged in triangular geometric shapes"""

    return (n*(n+1)/2)


def cube(n):
    """Returns the nth term of the numbers that
    can be arranged in triangular geometric shapes"""

    return n ** 3
